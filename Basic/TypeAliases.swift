typealias EmployeeID = Int

let userID: EmployeeID = 12345
print("User Id is \(userID) \n Type of userID is \(type(of:userID))")

/*output
User Id is 12345 
Type of userID is Int
*/

typealias EmployeeName = String
let userName: EmployeeName = "RAKESH YADAV"
print("User Name is \(userName) \n Type of userName is \(type(of:userName))")

/*output
User Name is RAKESH YADAV 
Type of userName is String
*/
